class ArithmeticOp
{
	public static void main(String[] args)
	{
		int a = 20;
		int b = 10;
		int c = a+b;
		int d = a-b;
		int e = a*b;
		int f = a/b;
		int g = a%b;
		int h = g++;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		
	}
}